package com.mycompany.sunco_cennik;

/**
 *
 * @author Hubert
 */
public class OrderComponents {

    public OrderComponents(String name, Double size, String type, Double price) {
        this.name = name;
        this.size = size;
        this.type = type;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getSize() {
        return size;
    }

    public void setSize(Double size) {
        this.size = size;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    private String name = "";
    private Double size = 0.0;
    private String type = "";
    private Double price = 0.0;
}
